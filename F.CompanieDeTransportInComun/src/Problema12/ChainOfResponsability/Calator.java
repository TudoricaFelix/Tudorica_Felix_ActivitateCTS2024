package Problema12.ChainOfResponsability;

public class Calator {
    private String nume;
    private int distanta;

    public Calator(String nume, int distanta) {
        this.nume = nume;
        this.distanta = distanta;
    }

    public String getNume() {
        return nume;
    }

    public int getDistanta() {
        return distanta;
    }
}
